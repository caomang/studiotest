package cn.sbx.deeper.moblie.domian;

/**
 * 日程个人类
 * 
 * @author 王克
 * 
 */
public class SchedulePersonalData {

	private String itemid;// 日程id
	private String title;// 日程标题
	private String startdate;// 日程开始时间
	private String enddate;// 日程结束时间
	private String localtion;// 日程地点
	private String status;// 日程状态
	private String creator;// 日程创建人
	private String organizer;// 日程主办处室
	private String scope;// 日程参加范围
	private String operation;// 日程操作项
	public String getItemid() {
		return itemid;
	}
	public void setItemid(String itemid) {
		this.itemid = itemid;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getStartdate() {
		return startdate;
	}
	public void setStartdate(String startdate) {
		this.startdate = startdate;
	}
	public String getEnddate() {
		return enddate;
	}
	public void setEnddate(String enddate) {
		this.enddate = enddate;
	}
	public String getLocaltion() {
		return localtion;
	}
	public void setLocaltion(String localtion) {
		this.localtion = localtion;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getOrganizer() {
		return organizer;
	}
	public void setOrganizer(String organizer) {
		this.organizer = organizer;
	}
	public String getScope() {
		return scope;
	}
	public void setScope(String scope) {
		this.scope = scope;
	}
	public String getOperation() {
		return operation;
	}
	public void setOperation(String operation) {
		this.operation = operation;
	}
	


}
